package com.luohuasheng.convert;

public class BaseResult {
    /**
     * 结果编码
     */
    private String code;

    /**
     * 结果信息
     */
    private String msg;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
